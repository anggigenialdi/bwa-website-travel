const mongoose = require('mongoose')

const memberSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true
  },
  lastName: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phoneNumber: {
    type: Number,
    required: true
  },
})

const Member = mongoose.model('Member', memberSchema);

module.exports = Member;